package com.pojo;

public class POJO {
	private String user;
	private String password;
	
	
	
	public POJO(String user, String password) {
		super();
		this.user=user;
		this.password=password;
	
	}


	public String getUser() {
		return user;
	}
	public String getPassword() {
		return password;
	}


	

}
